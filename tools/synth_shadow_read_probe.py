def _environment_fact_check() -> dict[str, Any]:
    """核查真实 data/canvas / canvases 表 / shadow_diff 状态；只读，不改。"""

    facts: dict[str, Any] = {}

    canvas_dir = REPO_ROOT / "data" / "canvas"
    facts["data/canvas exists"] = canvas_dir.exists()
    if canvas_dir.exists():
        facts["data/canvas *.json count"] = len(list(canvas_dir.glob("*.json")))
    else:
        facts["data/canvas *.json count"] = 0

    canvases_dir = REPO_ROOT / "data" / "canvases"
    facts["data/canvases exists"] = canvases_dir.exists()
    if canvases_dir.exists():
        facts["data/canvases *.json count"] = len(list(canvases_dir.glob("*.json")))

    db_path = REPO_ROOT / "data" / "app.db"
    facts["data/app.db exists"] = db_path.exists()
    if db_path.exists():
        try:
            conn = sqlite3.connect(str(db_path))
            try:
                row = conn.execute(
                    "SELECT name FROM sqlite_master WHERE type='table' AND name='canvases'"
                ).fetchone()
                facts["canvases table exists in prod DB"] = bool(row)
                if row:
                    n = conn.execute("SELECT COUNT(*) FROM canvases").fetchone()[0]
                    facts["canvases table row count"] = int(n)
            finally:
                conn.close()
        except Exception as exc:  # pragma: no cover
            facts["canvases table probe error"] = str(exc)

    shadow_diff_canvas = REPO_ROOT / "data" / "shadow_diff" / "canvas"
    facts["data/shadow_diff/canvas exists"] = shadow_diff_canvas.exists()
    if shadow_diff_canvas.exists():
        facts["data/shadow_diff/canvas jsonl count"] = len(
            list(shadow_diff_canvas.glob("*.jsonl"))
        )

    return facts



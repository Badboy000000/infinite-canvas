ALLOWED_SCALES = (10, 50, 200, 500)


def main_cli(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="synth_shadow_read_probe",
        description=(
            "Canvas shadow_read 就绪度合成负载评估探针（synthetic load only）"
        ),
    )
    parser.add_argument(
        "--scale",
        type=int,
        default=50,
        help=f"合成 canvas 数量（允许 {ALLOWED_SCALES}，默认 50）",
    )
    parser.add_argument(
        "--output",
        type=str,
        required=True,
        help="JSON 报告输出路径",
    )
    parser.add_argument("--seed", type=int, default=1337)
    parser.add_argument("--loads", type=int, default=100)
    parser.add_argument("--saves", type=int, default=200)
    parser.add_argument("--tx-fail-iters", type=int, default=3)
    args = parser.parse_args(argv)

    if args.scale not in ALLOWED_SCALES:
        parser.error(f"--scale must be one of {ALLOWED_SCALES}, got {args.scale}")

    # 用 tempfile 独立目录避免污染任何真实数据
    import tempfile
    import shutil

    tmp_dir = Path(tempfile.mkdtemp(prefix="synth_probe_"))
    try:
        report = run_probe(
            scale=args.scale,
            seed=args.seed,
            loads=args.loads,
            saves=args.saves,
            tx_fail_iters=args.tx_fail_iters,
            tmp_path=tmp_dir,
        )
    finally:
        # 清掉整个 tmp 工作区
        shutil.rmtree(tmp_dir, ignore_errors=True)

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(
        json.dumps(report, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )

    # 简要打印
    print(f"[synth-probe] scale={args.scale} readiness={report['readiness_verdict']}")
    for k, v in report["scenario_verdicts"].items():
        print(f"  {k}: {v}")
    for obs in report["observations"]:
        print(
            f"  observation ({obs['severity']} · {obs['cb_candidate']}): "
            f"{obs['title']}"
        )
    print(f"[synth-probe] report -> {output_path}")

    return 0


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main_cli())

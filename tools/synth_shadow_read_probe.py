def run_probe(
    *,
    scale: int,
    seed: int,
    loads: int,
    saves: int,
    tx_fail_iters: int,
    tmp_path: Path,
) -> dict[str, Any]:
    rng = random.Random(seed)
    scenarios: dict[str, Any] = {}
    observations: list[dict[str, Any]] = []

    # --- Scenario A ---
    workspace_a = tmp_path / "scenA"
    workspace_a.mkdir()
    with _isolated_workspace(workspace_a):
        import main

        canvas_dir_a = Path(main.CANVAS_DIR)
        canvases_a = _seed_canvases_json(canvas_dir_a, random.Random(seed + 1), scale)
        scenarios["A_hit_rate"] = _scenario_A_hit_rate(
            canvas_dir_a, canvases_a, random.Random(seed + 2), loads=loads
        )

    # --- Scenario B ---
    workspace_b = tmp_path / "scenB"
    workspace_b.mkdir()
    with _isolated_workspace(workspace_b):
        import main

        canvas_dir_b = Path(main.CANVAS_DIR)
        canvases_b = _seed_canvases_json(canvas_dir_b, random.Random(seed + 3), scale)
        scenarios["B_diff_rate"] = _scenario_B_diff_rate(
            canvas_dir_b, canvases_b, random.Random(seed + 4), loads=loads
        )

    # --- Scenario C ---
    workspace_c = tmp_path / "scenC"
    workspace_c.mkdir()
    with _isolated_workspace(workspace_c):
        import main

        canvas_dir_c = Path(main.CANVAS_DIR)
        canvases_c = _seed_canvases_json(canvas_dir_c, random.Random(seed + 5), scale)
        scenarios["C_write_latency"] = _scenario_C_write_latency(
            canvas_dir_c, canvases_c, random.Random(seed + 6), saves=saves
        )

    # --- Scenario D ---
    workspace_d = tmp_path / "scenD"
    workspace_d.mkdir()
    with _isolated_workspace(workspace_d):
        import main

        canvas_dir_d = Path(main.CANVAS_DIR)
        canvases_d = _seed_canvases_json(
            canvas_dir_d, random.Random(seed + 7), max(scale, tx_fail_iters + 1)
        )
        scenarios["D_tx_fail_safe"] = _scenario_D_tx_fail_safe(
            canvas_dir_d, canvases_d, tx_fail_iters=tx_fail_iters
        )

    # --- observations / CB-P5-08 candidates ---
    # asymmetric normalizer: single-canvas JSON vs full-DB snapshot
    scen_a = scenarios["A_hit_rate"]
    if scen_a.get("records_with_missing_in_json_nonempty", 0) > 0:
        observations.append(
            {
                "cb_candidate": "CB-P5-08",
                "severity": "P2",
                "title": (
                    "shadow_read canvas normalizer 与 DB snapshot 结构非对称 · "
                    "每次 load 都把'DB 里其它 canvas'算作 missing_in_json"
                ),
                "detail": (
                    "app.shadow_read.runner._normalize_json_canvas 只返回"
                    " {loaded_id: {...}} 单元素 dict，而 _load_db_snapshot 返回"
                    "整表 rows。所以任一次 load_canvas 触发的 shadow_read 记录都会"
                    "把 '当前不是被 load 的 canvas' 全部记录到 missing_in_json——"
                    "这是 canvas 域独有的结构非对称（其它 4 个 domain load 的都是"
                    "整表/整集合）。不算数据丢失，但会给运维观察增加 O(N) 噪声，"
                    "降低 alert 信噪比。建议 Wave 3-K 承接：canvas 域"
                    "single-id load 时只比对'同一 legacy_id'的字段级差异，"
                    "或者在 shadow_read layer 里增加 'load_scope=single_id' 语义。"
                ),
                "evidence_key": "scenarios.A_hit_rate.records_with_missing_in_json_nonempty",
            }
        )

    # created_at/updated_at datatype drift (already documented in existing test)
    if scen_a["field_diff_count_stats"]["mean"] >= 1.0:
        observations.append(
            {
                "cb_candidate": "CB-P5-08",
                "severity": "P3",
                "title": (
                    "created_at/updated_at 类型转换稳定触发 field_diffs（既知）"
                ),
                "detail": (
                    "JSON epoch-ms int vs DB DateTime ISO 字符串。"
                    "tests/shadow_read/test_canvas_shadow.py:test_shadow_enabled_no_diff_when_db_matches "
                    "已经承认此行为不算失败。运维监控应把 field 名单里的 "
                    "created_at / updated_at 加白名单过滤，否则每一次 load "
                    "都会产生 field_diffs 记录。"
                ),
                "evidence_key": "scenarios.A_hit_rate.field_diff_count_stats",
            }
        )

    scen_d = scenarios["D_tx_fail_safe"]
    if scen_d["saves_bubbled_exception"] > 0:
        observations.append(
            {
                "cb_candidate": "CB-P5-08",
                "severity": "P0",
                "title": "shadow_write 失败隔离契约破裂",
                "detail": (
                    "sqlite BEGIN EXCLUSIVE 持锁时 canvas_store.save_canvas "
                    "上抛异常，与 P0 硬约束（shadow write 内部异常不冒泡到主"
                    "写路径）不符。"
                ),
                "evidence_key": "scenarios.D_tx_fail_safe.saves_bubbled_exception",
            }
        )

    if scen_d["json_primary_write_files_actually_updated"] != scen_d["iterations"]:
        observations.append(
            {
                "cb_candidate": "CB-P5-08",
                "severity": "P0",
                "title": "JSON 主写在 DB 锁竞争下未成功落盘",
                "detail": (
                    "sqlite BEGIN EXCLUSIVE 持锁时 JSON 主写文件更新数量与"
                    "迭代次数不匹配，说明主写路径受 DB 锁影响。"
                    "这与 CANVAS_PRIMARY_WRITE=json 模式下'主写和 DB 完全解耦'"
                    "的契约不符。"
                ),
                "evidence_key": "scenarios.D_tx_fail_safe.json_primary_write_files_actually_updated",
            }
        )

    # shadow write 在 DB 锁竞争下 stall 5s（busy_timeout=5000 PRAGMA）
    per_iter = scen_d.get("per_iter_latency_ms", [])
    if per_iter and max(per_iter) >= 4000:
        observations.append(
            {
                "cb_candidate": "CB-P5-08",
                "severity": "P1",
                "title": (
                    "DB 锁竞争下 save_canvas 每次 stall ~5s（shadow_write "
                    "busy_timeout PRAGMA 语义）"
                ),
                "detail": (
                    "app/db/engine.py 注入 `PRAGMA busy_timeout = 5000`；"
                    "shadow_write 在 DB 被 BEGIN EXCLUSIVE 锁定时会阻塞到 "
                    "busy_timeout 才 raise。JSON 主路径本身不受影响（异常已"
                    "隔离，未冒泡），但 save_canvas 的墙钟延迟 = JSON 主写 + "
                    "shadow_write 5s wait。这是 CANVAS_PRIMARY_WRITE=json 模式下"
                    "可接受的（毕竟异常不冒泡），但**切到 db 主写后，任何 DB "
                    "级锁竞争都会直接把 save_canvas 阻塞到 5s，用户可见回归"
                    "风险显著**。建议 Wave 3-K 承接 CANVAS_PRIMARY_WRITE=db "
                    "前：降低 canvas_writer 的 busy_timeout 到 500-1000ms + "
                    "把长时间锁竞争纳入 SLI/SLO 观察。"
                ),
                "evidence_key": "scenarios.D_tx_fail_safe.per_iter_latency_ms",
            }
        )

    # readiness verdict aggregation
    verdicts = {k: v["verdict"] for k, v in scenarios.items()}
    all_pass = all(v == "PASS" for v in verdicts.values())
    any_p0 = any(o.get("severity") == "P0" for o in observations)
    any_p1 = any(o.get("severity") == "P1" for o in observations)

    if any_p0:
        readiness = "RED"
    elif not all_pass or any_p1:
        readiness = "YELLOW"
    else:
        readiness = "GREEN"

    return {
        "schema": SCHEMA_VERSION,
        "synthetic_load_declaration": SYNTHETIC_DECLARATION,
        "generated_at_utc": _dt.datetime.now(_dt.timezone.utc).isoformat(),
        "scale": scale,
        "seed": seed,
        "loads_per_scenario": loads,
        "saves_scenario_C": saves,
        "tx_fail_iters_scenario_D": tx_fail_iters,
        "environment": _environment_fact_check(),
        "scenarios": scenarios,
        "scenario_verdicts": verdicts,
        "observations": observations,
        "readiness_verdict": readiness,
        "readiness_verdict_definition": {
            "GREEN": "所有 4 场景 PASS 且无 P0/P1 观察项 → 可放行 CANVAS_PRIMARY_WRITE=db",
            "YELLOW": "有 P1 观察项 或 部分场景 FAIL（无 P0）→ 有条件放行（先修 P1）",
            "RED": "存在 P0 观察项 → 不建议放行 db 主写",
        },
    }



def _make_synthetic_canvas(rng: random.Random, canvas_id: str) -> dict[str, Any]:
    """造一个 mock canvas；nodes/connections 数量 5-30 随机。"""

    n_nodes = rng.randint(5, 30)
    n_conns = rng.randint(5, 30)
    now_ms = int(time.time() * 1000)
    return {
        "id": canvas_id,
        "title": f"Synthetic Canvas {canvas_id}",
        "kind": rng.choice(["classic", "smart"]),
        "project": "synth-default",
        "owner": "synth-probe",
        "pinned": rng.choice([True, False]),
        "created_at": now_ms - rng.randint(0, 86400 * 1000),
        "updated_at": now_ms,
        "deleted_at": None,
        "revision": rng.randint(0, 5),
        "base_updated_at": None,
        "nodes": [
            {"id": f"n{i}", "type": "generator", "x": i * 10, "y": i * 20}
            for i in range(n_nodes)
        ],
        "connections": [
            {"from": f"n{i}", "to": f"n{(i + 1) % max(n_nodes, 1)}"}
            for i in range(n_conns)
        ],
    }


def _write_canvas_json(canvas: dict[str, Any], canvas_dir: Path) -> None:
    path = canvas_dir / f"{canvas['id']}.json"
    path.write_text(json.dumps(canvas, ensure_ascii=False, indent=2), encoding="utf-8")


def _percentiles(values: list[float]) -> dict[str, float]:
    if not values:
        return {"p50_ms": 0.0, "p95_ms": 0.0, "p99_ms": 0.0, "max_ms": 0.0, "count": 0}
    sorted_v = sorted(values)
    n = len(sorted_v)

    def _pct(p: float) -> float:
        if n == 1:
            return sorted_v[0]
        k = (n - 1) * p
        f = int(k)
        c = min(f + 1, n - 1)
        return sorted_v[f] + (sorted_v[c] - sorted_v[f]) * (k - f)

    return {
        "p50_ms": round(_pct(0.50) * 1000, 3),
        "p95_ms": round(_pct(0.95) * 1000, 3),
        "p99_ms": round(_pct(0.99) * 1000, 3),
        "max_ms": round(sorted_v[-1] * 1000, 3),
        "count": n,
    }



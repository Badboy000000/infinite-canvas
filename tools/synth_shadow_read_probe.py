@contextlib.contextmanager
def _isolated_workspace(tmp_path: Path):
    """把 main.DATA_DIR / CANVAS_DIR / DATA_DB_PATH 指向 tmp_path。

    对应 `tests/shadow_read/_helpers.py::isolated_shadow_env` 的 pytest fixture
    实现，但走 setattr/restore 手工模式，可在 CLI 里使用。
    """

    import main
    from app.db import engine as db_engine
    from app.db import session as db_session

    saved = {
        "DATA_DIR": main.DATA_DIR,
        "CANVAS_DIR": main.CANVAS_DIR,
        "DATA_DB_PATH": main.DATA_DB_PATH,
    }
    canvas_dir = tmp_path / "canvases"
    canvas_dir.mkdir(parents=True, exist_ok=True)

    main.DATA_DIR = str(tmp_path)
    main.CANVAS_DIR = str(canvas_dir)
    main.DATA_DB_PATH = str(tmp_path / "shadow.db")
    db_engine.reset_engine()
    db_session._SessionLocal = None

    try:
        yield tmp_path
    finally:
        main.DATA_DIR = saved["DATA_DIR"]
        main.CANVAS_DIR = saved["CANVAS_DIR"]
        main.DATA_DB_PATH = saved["DATA_DB_PATH"]
        db_engine.reset_engine()
        db_session._SessionLocal = None


@contextlib.contextmanager
def _env_scope(env: dict[str, str | None]):
    """临时 set/unset env vars；退出时 restore。"""

    saved: dict[str, str | None] = {k: os.environ.get(k) for k in env}
    for k, v in env.items():
        if v is None:
            os.environ.pop(k, None)
        else:
            os.environ[k] = v
    try:
        yield
    finally:
        for k, v in saved.items():
            if v is None:
                os.environ.pop(k, None)
            else:
                os.environ[k] = v



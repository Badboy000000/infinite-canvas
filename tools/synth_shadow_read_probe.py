def _seed_canvases_json(
    canvas_dir: Path, rng: random.Random, scale: int
) -> list[dict[str, Any]]:
    """写 N 个 mock canvas JSON 到 canvas_dir；返回全部 dict list。"""

    canvases: list[dict[str, Any]] = []
    for i in range(scale):
        canvas_id = f"synth_c_{i:05d}"
        c = _make_synthetic_canvas(rng, canvas_id)
        _write_canvas_json(c, canvas_dir)
        canvases.append(c)
    return canvases


def _import_all_to_db(canvas_dir: Path) -> int:
    """把 canvas_dir 全导 DB；返回 inserted 数量。"""

    from app.data_import import import_domain
    from tests.shadow_read._helpers import migrate_baseline

    migrate_baseline(canvas_dir.parent)
    outcome = import_domain("canvas", source_path=str(canvas_dir), dry_run=False)
    return int(outcome.inserted)


def _import_partial_to_db(canvas_dir: Path, keep_ids: set[str]) -> int:
    """选择性导入 DB：只导 keep_ids 中的 canvas。做法：临时目录只放 keep_ids。"""

    from app.data_import import import_domain
    from tests.shadow_read._helpers import migrate_baseline

    migrate_baseline(canvas_dir.parent)
    # 造一个临时子目录，只放要导的
    subset_dir = canvas_dir.parent / "subset_import"
    subset_dir.mkdir(parents=True, exist_ok=True)
    for cid in keep_ids:
        src = canvas_dir / f"{cid}.json"
        if src.exists():
            (subset_dir / f"{cid}.json").write_text(
                src.read_text(encoding="utf-8"), encoding="utf-8"
            )
    outcome = import_domain("canvas", source_path=str(subset_dir), dry_run=False)
    return int(outcome.inserted)


def _scenario_A_hit_rate(
    canvas_dir: Path,
    canvases: list[dict[str, Any]],
    rng: random.Random,
    loads: int,
) -> dict[str, Any]:
    """场景 A：N 个双写；100 次随机 load；期望 missing_in_db 全空。"""

    from app.stores import canvas_store

    inserted = _import_all_to_db(canvas_dir)
    all_ids = [c["id"] for c in canvases]

    hits = 0
    misses = 0
    field_diff_counts: list[int] = []
    load_latency: list[float] = []

    with _env_scope(
        {"SHADOW_READ_CANVAS": "true", "CANVAS_PRIMARY_WRITE": "json"}
    ):
        for _ in range(loads):
            cid = rng.choice(all_ids)
            t0 = time.perf_counter()
            try:
                canvas_store.load_canvas(cid)
            except Exception:
                # HTTPException from 404 shouldn't occur here (all files exist)
                misses += 1
                continue
            load_latency.append(time.perf_counter() - t0)

    # 读回 shadow_diff/canvas/*.jsonl 统计
    diff_root = canvas_dir.parent / "shadow_diff" / "canvas"
    lines: list[dict[str, Any]] = []
    if diff_root.exists():
        for f in diff_root.glob("*.jsonl"):
            for line in f.read_text(encoding="utf-8").splitlines():
                line = line.strip()
                if not line:
                    continue
                try:
                    lines.append(json.loads(line))
                except Exception:
                    pass

    hits_no_missing_in_db = sum(1 for rec in lines if not rec.get("missing_in_db"))
    records_with_missing_in_db = sum(1 for rec in lines if rec.get("missing_in_db"))
    records_with_missing_in_json = sum(
        1 for rec in lines if rec.get("missing_in_json")
    )
    total = len(lines)
    for rec in lines:
        field_diff_counts.append(len(rec.get("field_diffs", [])))

    return {
        "description": (
            "N canvas 全部 JSON + DB 双写；100 次随机 load；"
            "期望 missing_in_db 全空（field_diffs 会因 created_at/updated_at 类型转换非零，属既知行为）"
        ),
        "db_inserted": inserted,
        "loads_attempted": loads,
        "shadow_diff_records_written": total,
        "records_with_missing_in_db_empty": hits_no_missing_in_db,
        "records_with_missing_in_db_nonempty": records_with_missing_in_db,
        "records_with_missing_in_json_nonempty": records_with_missing_in_json,
        "hit_rate": (
            round(hits_no_missing_in_db / total, 4) if total > 0 else None
        ),
        "field_diff_count_stats": {
            "mean": (
                round(sum(field_diff_counts) / len(field_diff_counts), 2)
                if field_diff_counts
                else 0.0
            ),
            "max": max(field_diff_counts) if field_diff_counts else 0,
        },
        "load_latency": _percentiles(load_latency),
        "target_hit_rate": ">= 0.99",
        "verdict": (
            "PASS"
            if total > 0 and hits_no_missing_in_db / total >= 0.99
            else "FAIL"
        ),
    }


def _scenario_B_diff_rate(
    canvas_dir: Path,
    canvases: list[dict[str, Any]],
    rng: random.Random,
    loads: int,
) -> dict[str, Any]:
    """场景 B：N JSON 全写；DB 只写 90%；100 次随机 load；期望 ~10% missing_in_db。"""

    from app.stores import canvas_store

    all_ids = [c["id"] for c in canvases]
    keep_count = max(1, int(round(len(all_ids) * 0.9)))
    keep_ids = set(rng.sample(all_ids, k=keep_count))
    missing_ids = set(all_ids) - keep_ids

    inserted = _import_partial_to_db(canvas_dir, keep_ids)

    loads_missing_expected = 0
    loads_hit_expected = 0

    with _env_scope(
        {"SHADOW_READ_CANVAS": "true", "CANVAS_PRIMARY_WRITE": "json"}
    ):
        for _ in range(loads):
            cid = rng.choice(all_ids)
            if cid in missing_ids:
                loads_missing_expected += 1
            else:
                loads_hit_expected += 1
            try:
                canvas_store.load_canvas(cid)
            except Exception:
                pass

    diff_root = canvas_dir.parent / "shadow_diff" / "canvas"
    lines: list[dict[str, Any]] = []
    if diff_root.exists():
        for f in diff_root.glob("*.jsonl"):
            for line in f.read_text(encoding="utf-8").splitlines():
                line = line.strip()
                if not line:
                    continue
                try:
                    lines.append(json.loads(line))
                except Exception:
                    pass

    records_with_missing_in_db = sum(1 for rec in lines if rec.get("missing_in_db"))
    total = len(lines)

    diff_rate = round(records_with_missing_in_db / total, 4) if total > 0 else None
    expected_ratio = round(loads_missing_expected / loads, 4) if loads > 0 else None

    return {
        "description": (
            "N JSON 全写；DB 90% (缺 10%)；100 次随机 load；"
            "期望 shadow diff 中 ~10% 记录含 missing_in_db"
        ),
        "keep_ratio_target": 0.9,
        "db_inserted": inserted,
        "missing_ids_count": len(missing_ids),
        "loads_attempted": loads,
        "loads_landed_on_missing": loads_missing_expected,
        "loads_landed_on_hit": loads_hit_expected,
        "expected_diff_rate": expected_ratio,
        "shadow_diff_records_written": total,
        "records_with_missing_in_db_nonempty": records_with_missing_in_db,
        "observed_diff_rate": diff_rate,
        "delta_vs_expected": (
            round(abs((diff_rate or 0.0) - (expected_ratio or 0.0)), 4)
            if diff_rate is not None and expected_ratio is not None
            else None
        ),
        "target_delta_bound": "<= 0.05",
        "verdict": (
            "PASS"
            if diff_rate is not None
            and expected_ratio is not None
            and abs(diff_rate - expected_ratio) <= 0.05
            else "FAIL"
        ),
    }


def _scenario_C_write_latency(
    canvas_dir: Path,
    canvases: list[dict[str, Any]],
    rng: random.Random,
    saves: int,
) -> dict[str, Any]:
    """场景 C：save_canvas 循环 200 次（SHADOW_WRITE_CANVAS enable）；测 P50/P95/P99。"""

    from app.stores import canvas_store
    from tests.shadow_read._helpers import migrate_baseline

    migrate_baseline(canvas_dir.parent)

    # 先给 canvas_store 一批 seed（复用 seeded canvases）
    save_latency: list[float] = []
    errors_bubbled = 0

    with _env_scope(
        {"SHADOW_WRITE_CANVAS": "true", "CANVAS_PRIMARY_WRITE": "json"}
    ):
        for i in range(saves):
            # 循环使用已经种下来的 canvas；每次 clone 一份并 tweak title
            base = canvases[i % len(canvases)]
            snap = dict(base)
            snap["id"] = base["id"]  # 保持 id
            snap["title"] = f"{base['title']} · iter {i}"
            t0 = time.perf_counter()
            try:
                canvas_store.save_canvas(snap)
            except Exception:
                errors_bubbled += 1
                continue
            save_latency.append(time.perf_counter() - t0)

    stats = _percentiles(save_latency)

    return {
        "description": (
            "SHADOW_WRITE_CANVAS=true；save_canvas 循环 200 次；"
            "记录 P50/P95/P99；期望 P95 <= 500ms（治理方案 §PR-6 P1 硬约束）"
        ),
        "saves_attempted": saves,
        "saves_bubbled_error": errors_bubbled,
        "latency_ms": stats,
        "target_p95_ms": 500.0,
        "verdict": "PASS" if stats["p95_ms"] <= 500.0 and errors_bubbled == 0 else "FAIL",
    }


def _scenario_D_tx_fail_safe(
    canvas_dir: Path,
    canvases: list[dict[str, Any]],
    tx_fail_iters: int,
) -> dict[str, Any]:
    """场景 D：sqlite BEGIN EXCLUSIVE 锁 DB；测 shadow write fail-safe（异常不上抛）。"""

    from app.stores import canvas_store
    from tests.shadow_read._helpers import migrate_baseline

    migrate_baseline(canvas_dir.parent)

    # 找到 DB 路径 + 提前压一条 baseline row 避免"表空导致 SELECT 之外都没竞争"的误判
    import main

    db_path = main.DATA_DB_PATH

    # 先在无锁状态下 pre-warm 一次 shadow write 确保 canvases 表有行
    with _env_scope(
        {"SHADOW_WRITE_CANVAS": "true", "CANVAS_PRIMARY_WRITE": "json"}
    ):
        pre_warm = dict(canvases[0])
        pre_warm["title"] = "prewarm"
        canvas_store.save_canvas(pre_warm)

    lock_conn = sqlite3.connect(db_path, timeout=0.1, isolation_level=None)
    # 设短的 busy_timeout 让 shadow write 快速抢不到锁
    lock_conn.execute("PRAGMA busy_timeout = 200")

    save_bubbled_errors = 0
    saves_completed = 0
    per_iter_ms: list[float] = []

    try:
        lock_conn.execute("BEGIN EXCLUSIVE")
        with _env_scope(
            {"SHADOW_WRITE_CANVAS": "true", "CANVAS_PRIMARY_WRITE": "json"}
        ):
            for i in range(tx_fail_iters):
                snap = dict(canvases[(i + 1) % len(canvases)])
                snap["title"] = f"lockprobe iter {i}"
                t0 = time.perf_counter()
                try:
                    canvas_store.save_canvas(snap)
                    saves_completed += 1
                except Exception:
                    save_bubbled_errors += 1
                per_iter_ms.append((time.perf_counter() - t0) * 1000)
    finally:
        try:
            lock_conn.execute("COMMIT")
        except Exception:
            try:
                lock_conn.execute("ROLLBACK")
            except Exception:
                pass
        lock_conn.close()

    # 读回 shadow_diff/canvas_write/*.jsonl 看是否有 failure 记录
    diff_root = Path(main.DATA_DIR) / "shadow_diff" / "canvas_write"
    failure_records = 0
    sample_failure_reason: str | None = None
    if diff_root.exists():
        for f in diff_root.glob("*.jsonl"):
            for line in f.read_text(encoding="utf-8").splitlines():
                line = line.strip()
                if not line:
                    continue
                try:
                    rec = json.loads(line)
                except Exception:
                    continue
                failure_records += 1
                if sample_failure_reason is None:
                    sample_failure_reason = str(rec.get("error") or rec.get("fallback_reason"))

    # 验证：JSON 主写文件已被更新（因为 main.save_canvas 走 JSON 主写，不受锁影响）
    json_files_written = 0
    for c in canvases[1 : tx_fail_iters + 1]:
        p = canvas_dir / f"{c['id']}.json"
        if p.exists():
            content = json.loads(p.read_text(encoding="utf-8"))
            if content.get("title", "").startswith("lockprobe iter"):
                json_files_written += 1

    fail_safe_ok = save_bubbled_errors == 0 and saves_completed == tx_fail_iters
    return {
        "description": (
            "sqlite BEGIN EXCLUSIVE 持锁；shadow write 无法拿到写锁；"
            "验证 save_canvas 主路径（JSON）不受影响 + shadow_write 异常不上抛"
        ),
        "iterations": tx_fail_iters,
        "saves_completed_no_exception": saves_completed,
        "saves_bubbled_exception": save_bubbled_errors,
        "shadow_write_failure_records_logged": failure_records,
        "json_primary_write_files_actually_updated": json_files_written,
        "sample_failure_reason": sample_failure_reason,
        "per_iter_latency_ms": [round(x, 3) for x in per_iter_ms],
        "target_fail_safe": "save_bubbled_exception == 0 AND JSON files updated",
        "verdict": "PASS" if fail_safe_ok and json_files_written == tx_fail_iters else "FAIL",
    }


